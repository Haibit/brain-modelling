function regions = get_atlas_region(obj)

nlabels = length(obj.label);
regions = struct('name','','order',num2cell(zeros(nlabels,1)));
for i=1:nlabels
    switch obj.label{i}
        case {'Frontal Sup L','Frontal Sup R',...
                'Frontal Sup Orb L','Frontal Sup Orb R',...
                'Frontal Mid L','Frontal Mid R',...
                'Frontal Mid Orb L','Frontal Mid Orb R',...
                'Frontal Inf Oper L','Frontal Inf Oper R',...
                'Frontal Inf Tri L','Frontal Inf Tri R',...
                'Frontal Inf Orb L','Frontal Inf Orb R',...
                'Frontal Sup Medial L','Frontal Sup Medial R',...
                'Frontal Med Orb L','Frontal Med Orb R',...
                'Rolandic Oper L','Rolandic Oper R',...
                'Rectus L','Rectus R',...
                'Olfactory L','Olfactory R'}
            regions(i).name = 'Prefrontal';
            regions(i).order = 1;
            
        case {'Precentral L','Precentral R',...
                'Supp Motor Area L','Supp Motor Area R'}
            regions(i).name = 'Motor';
            regions(i).order = 2;
            
        case {'Pallidum L','Pallidum R',...
                'Caudate L','Caudate R',...
                'Putamen L','Putamen R',...
                'Thalamus L','Thalamus R'}
            regions(i).name = 'Basal Ganglia';
            regions(i).order = 3;
            
        case {'Insula L','Insula R'}
            regions(i).name = 'Insula';
            regions(i).order = 4;
            
        case {'Parietal Sup L','Parietal Sup R',...
                'Parietal Inf L','Parietal Inf R',...
                'SupraMarginal L','SupraMarginal R',...
                'Angular L','Angular R',...
                'Precuneus L','Precuneus R',...
                'Postcentral L','Postcentral R',...
                'Paracentral Lobule L','Paracentral Lobule R',}
            regions(i).name = 'Parietal';                
            regions(i).order = 5;
            
        case {'Temporal Mid R','Temporal Mid L',...
                'Temporal Inf R','Temporal Inf L',...
                'Temporal Pole Sup R','Temporal Pole Sup L',...
                'Temporal Pole Mid R','Temporal Pole Mid L'...
                'Temporal Sup R','Temporal Sup L',...
                'Heschl L','Heschl R'}
            regions(i).name = 'Temporal';
            regions(i).order = 6;
            
        case {'Occipital Sup L','Occipital Sup R',...
                'Occipital Mid L','Occipital Mid R',...
                'Occipital Inf L','Occipital Inf R',...
                'Cuneus L','Cuneus R',...
                'Fusiform L','Fusiform R',...
                'Lingual L','Lingual R',...
                'Calcarine L','Calcarine R'}
            regions(i).name = 'Occipital';
            regions(i).order = 7;
            
        case {'Hippocampus L','Hippocampus R',...
                'ParaHippocampal L','ParaHippocampal R',...
                'Amygdala L','Amygdala R',...
                'Cingulum Ant L','Cingulum Ant R',...
                'Cingulum Mid L','Cingulum Mid R',...
                'Cingulum Post L','Cingulum Post R'}
            regions(i).name = 'Limbic';
            regions(i).order = 8;
            
        case {'Cerebelum Crus1 L','Cerebelum Crus1 R',...
                'Cerebelum Crus2 L','Cerebelum Crus2 R',...
                'Cerebelum 3 L','Cerebelum 3 R',...
                'Cerebelum 4 5 L','Cerebelum 4 5 R',...
                'Cerebelum 6 L','Cerebelum 6 R',...
                'Cerebelum 7b L','Cerebelum 7b R',...
                'Cerebelum 8 L','Cerebelum 8 R',...
                'Cerebelum 9 L','Cerebelum 9 R',...
                'Cerebelum 10 L','Cerebelum 10 R',...
                'Vermis 1 2',...
                'Vermis 3',...
                'Vermis 4 5',...
                'Vermis 6',...
                'Vermis 7',...
                'Vermis 8',...
                'Vermis 9',...
                'Vermis 10'}
            regions(i).name = 'Cerebellum';
            regions(i).order = 9;
            
        otherwise
            fprintf('%s not assigned\n',obj.label{i});
    end
end

end